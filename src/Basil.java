public class Basil {
	public static void main(String[] args) {
		System.out.println(Math.abs(-26.7)); // Abousolute value. ex: 26.7 = 26.7 
		System.out.println(Math.ceil(7.4));// Ceiling round up. ex: 7.4 = 8
		System.out.println(Math.floor(7.4));// Floor round down. ex: 7.4 = 7
		System.out.println(Math.max(8.6,5.2));// max =8.6
		System.out.println(Math.min(8.6,5.2)); //min =5
		System.out.println(Math.pow(8,6)); //ex: 8^6
		System.out.println(Math.sqrt(9)); //Square root
	}

}
